function dias(num)
{
    if (num==1)
    {
        console.log("Domingo")
        return "domingo"
    }
    else if (num==2)
    {
        console.log("Segunda")
        return "Segunda"
    }
    else if (num==3)
    {
        console.log("Terça")
        return "Terça"
    }
    else if (num==4)
    {
        console.log("Quarta")
        return "Quarta"
    }
    else if (num==5)
    {
        console.log("Quinta")
        return "Quinta"
    }
    else if (num==6)
    {
        console.log("Sexta")
        return "Sexta"
    }
    else if (num==7)
    {
        console.log("Sabado")
        return "Sabado"
    }
    else
    {
        console.log("Número nao reconhecido")
        return "Numero não reconhecido"
    }

}
dias(3)